package com.homepractising;
//Literals 
//integral literals
public class Exercise5 {

	public static void main(String[] args) {
		//decimal
		 int a1=100;
		 System.out.println("decimal:"+a1);
       //binary 
		 int a2=0b10110101010101010101010101010101;
		 System.out.println("binary:"+a2);
	  //octal
		 int a3=01234;
		 //1*8^3+2*8^2+3*8^1+4*8^0
		 System.out.println("octal:"+a3);
	 //hexadecimal
		 int a4=0xa34df;
		//10*16^4+3*16^3+4*16^2+14*16^1+15*16^0
		 System.out.println("hexadecimal:"+a4);
//long literal		 
System.out.println("************************************");
		//decimal
		  long b1=100;
		 System.out.println("decimal:"+b1);
       //binary 
		 long b2=0b1011010101010101010101010101010101001010110101L;
		 System.out.println("binary:"+b2);
	  //octal
		 long b3= 1234567123456712345L;
		 //1*8^3+2*8^2+3*8^1+4*8^0
		 System.out.println("octal:"+b3);
	 //hexadecimal
		  long b4=0x123456789abcdefL;
		//10*16^4+3*16^3+4*16^2+14*16^1+15*16^0
		 System.out.println("hexadecimal:"+b4);
		  
		  
		 
	}

} 
